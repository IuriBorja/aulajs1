/*
 Operadores Lógicos

    && -> AND - Todas as expressões precisam ser verdadeiras para retornar true
    || -> OR - Todas as expressões precisam ser falsas para retornar false
    !  -> NOT - Inverte o valor
 */


    const expressaoAnd = true && true && true; // resultado: true
    const expressaoOr = false || true || false; // resultado: true

    console.log(expressaoAnd);
    console.log(expressaoOr);


    console.log(!false); // resultado : true

